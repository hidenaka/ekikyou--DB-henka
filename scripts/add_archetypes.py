def main():
    base_dir = Path(__file__).resolve().parent.parent
    json_path = base_dir / "data" / "diagnostic" / "hexagram_64.json"

    if not json_path.exists():
        print(f"ERROR: {json_path} が見つかりません")
        sys.exit(1)

    # JSON読み込み
    with open(json_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    hexagrams = data.get("hexagrams", {})

    # 統計カウンター
    updated = 0
    missing = []

    for name, hex_data in hexagrams.items():
        if name in ARCHETYPE_DATA:
            archetype, interpretation = ARCHETYPE_DATA[name]
            hex_data["archetype"] = archetype
            hex_data["modern_interpretation"] = interpretation
            updated += 1
        else:
            missing.append(name)

    # マッピングに存在するがhexagram_64.jsonにない卦の確認
    unmapped = [k for k in ARCHETYPE_DATA if k not in hexagrams]

    # 結果書き込み
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # レポート
    print(f"=== アーキタイプ追加完了 ===")
    print(f"更新済み: {updated} / {len(hexagrams)} 卦")

    if missing:
        print(f"\nWARNING: 以下の卦にアーキタイプデータがありません:")
        for m in missing:
            print(f"  - {m}")

    if unmapped:
        print(f"\nWARNING: 以下のアーキタイプデータがhexagram_64.jsonに該当卦なし:")
        for u in unmapped:
            print(f"  - {u}")

    if updated == len(hexagrams) and not missing and not unmapped:
        print("全64卦にアーキタイプが正常に追加されました。")


if __name__ == "__main__":
    main()
