def generate_full_master():
    """64卦の完全マスタデータを生成"""
    master = {}

    for hex_id, base in HEXAGRAM_BASE.items():
        interpretations = generate_interpretations(hex_id, base)

        master[str(hex_id)] = {
            "id": hex_id,
            "name": base["name"],
            "chinese": base["chinese"],
            "reading": base["reading"],
            "upper_trigram": base["upper"],
            "lower_trigram": base["lower"],
            "keyword": base["keyword"],
            "meaning": base["meaning"],
            "interpretations": interpretations
        }

    return master


